    @Test
    public void testToJSON() {
        System.out.println("toJSON");
        Plan plan = new Plan(1);
        Asignatura asignatura = new Asignatura(1);
        asignatura.setCodigoAsignatura("10000");
        asignatura.setNombreAsignatura("Asignatura");
        asignatura.setNivelAsignatura(1);
        Short s = 1;
        asignatura.setEsAnual(s);
        asignatura.setSctAsignatura(2);
        asignatura.setHorasTeoria(3);
        asignatura.setHorasEjercicio(4);
        asignatura.setHorasLaboratorio(5);
        asignatura.setResumenAsignatura("Resumen");

        Asignatura asigReq = new Asignatura();
        asigReq.setCodigoAsignatura("99999");
        ArrayList<Asignatura> asigsReq = new ArrayList<>();
        asigsReq.add(asigReq);
        
        Asignatura asigApe = new Asignatura();
        asigApe.setCodigoAsignatura("100000");
        ArrayList<Asignatura> asigsApe = new ArrayList<>();
        asigsApe.add(asigApe);
        
        asignatura.setAsignaturaCollection1(asigsApe);
        asignatura.setAsignaturaCollection(asigsReq);
        
        ArrayList<Asignatura> asignaturas = new ArrayList<>();
        asignaturas.add(asignatura);
        plan.setAsignaturaCollection(asignaturas);
        PlanController instance = new PlanController();
        String expResult = "var context = [{ \"nombre\": \"Asignatura\", \"id\": 10000, \"nivel\": 1, \"anual\": true, \"sct\": 2, \"t\": 3, \"e\": 4, \"l\": 5, \"resumen\": \"Resumen\", \"prerequisitos\": [99999], \"aperturas\": [100000] }];";
        String result = instance.toJSON(plan);
        assertEquals(expResult, result);
    }

